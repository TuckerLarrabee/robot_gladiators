var num = 5

switch(num) {
    case 1:
        console.log ("the variable was 1.");
        break;
    case 2:
        console.log ("the variable was 2.");
        break;
    case 3:
        console.log("The variable was 3");
        break;
    default:
        console.log("the variable was something else");
        break;
}

